import java.util.*;
class ArrayListDemo
{
	public static void main(String args[])
	{
		ArrayList <String> list1 = new ArrayList<String>();
		list1.add("Rohit");
		list1.add("Mukesh");
		list1.add("Radha");
		list1.add("Geeta");
		
		//using for each
		
	for (String s : list1)
	{
		System.out.println(s);
	}
	
	}
	}









